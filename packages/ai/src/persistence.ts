export interface TelemetryInput {
  threadId: string;
  /** Phase A — the owning user. */
  userId?: string | null;
  messageId: string | null;
  model: string;
  inputTokens: number;
  outputTokens: number;
  toolCalls: number;
  ms: number;
  /**
   * Row marker for non-assistant-turn telemetry. Legacy assistant-turn callers
   * omit this and the column stays `null`. Title_Generator emits one of:
   * `'title_generated' | 'title_failed' | 'title_skipped_budget'`. The Phase
   * 7a router emits `'routing_<domain>'`. The Phase 7c planner emits one of:
   * `'plan_generated' | 'plan_skipped_budget' | 'plan_failed'`.
   */
  kind?:
    | 'title_generated'
    | 'title_failed'
    | 'title_skipped_budget'
    | 'routing_fundamental'
    | 'routing_technical'
    | 'routing_summary'
    | 'routing_vision'
    | 'routing_generic'
    | 'plan_generated'
    | 'plan_skipped_budget'
    | 'plan_failed'
    // P3: per-specialist multi-agent telemetry breadcrumbs.
    | 'multi_specialist_technical'
    | 'multi_specialist_fundamental'
    | 'multi_specialist_risk'
    | 'multi_specialist_sentiment'
    | 'multi_specialist_decision';
}

export async function recordTelemetry(t: TelemetryInput): Promise<void> {
  // Phase 3 §3.11 — the __system__ fallback is retained ONLY because the
  // userId column is NOT NULL with a FK to users.id, and __system__ exists
  // as a seeded row (migration 0009). Callers should always pass t.userId.
  // The fallback ensures we don't lose telemetry for paths that haven't
  // been updated yet, but it should be removed once all callers pass userId.
  const userId = t.userId ?? '__system__';
  await getDb()
    .insert(schema.chatTelemetry)
    .values({
      userId,
      threadId: t.threadId,
      messageId: t.messageId,
      model: t.model,
      inputTokens: t.inputTokens,
      outputTokens: t.outputTokens,
      toolCalls: t.toolCalls,
      ms: t.ms,
      estCostUsd: estimateCostUsd(t.model, t.inputTokens, t.outputTokens),
      kind: t.kind ?? null,
    });
}

