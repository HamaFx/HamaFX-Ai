// ---------------------------------------------------------------------------
// Per-tool telemetry (Phase 7b)
// ---------------------------------------------------------------------------

export interface ToolTelemetryInput {
  threadId: string | null;
  /** Phase A — the owning user. */
  userId?: string | null;
  messageId: string | null;
  tool: string;
  ms: number;
  ok: boolean;
  errorCode?: string | null;
  /** F7-obs — estimated character count of the tool's output. */
  outputChars?: number | null;
}

export async function recordToolTelemetry(t: ToolTelemetryInput): Promise<void> {
  try {
    // Phase 3 §3.11 — same pattern as recordTelemetry: __system__ fallback
    // retained for the NOT NULL FK column, but callers should pass userId.
    const userId = t.userId ?? '__system__';
    await getDb()
      .insert(schema.chatToolTelemetry)
      .values({
        userId,
        threadId: t.threadId,
        messageId: t.messageId,
        tool: t.tool,
        ms: t.ms,
        ok: t.ok,
        errorCode: t.errorCode ?? null,
        outputChars: t.outputChars ?? null,
      });
  } catch (err) {
    // Tool-telemetry failures must never crash a chat turn.
    perlog.warn('tool telemetry insert failed', { err: String(err) });
  }
}
